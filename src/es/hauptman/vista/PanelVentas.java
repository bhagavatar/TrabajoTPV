    /**
     * Método para sumar los valores del subtotal de la tabla.
     * @return int
     */
    public double getSum(){
        int rowCount = tablaVentas.getRowCount();
        double sum = 0;
        for(int i=0;i < rowCount;i++){
            sum=sum+Double.parseDouble(tablaVentas.getValueAt(i, 4).toString());
            
        }
        return sum;
        
    
    }

